package com.ohgiraffers.section01.polymorphism;

public class 너구려 extends 동물 {

    @Override
    public void 먹기() {
        System.out.println("깨끗하게 씻어서 먹습니다");
    }

    @Override
    public void 뛰기() {
        System.out.println("농심에 취직하러 뛰어갑니다.");
    }

    @Override
    public void 울부짖기() {
        System.out.println("너구려가 부릅니다. DO It ");
    }

    public void 코딩(){
        System.out.println("코딩으로 자본을 벌고 있습니다.");
    }
}



